class Task {
  final int id;
  final String name;
  final String type;
  final int age;
  final String breed;
  final String photoPath;
  final double weight;
  final String healthStatus;
  final int status;

  Task({
    required this.id,
    required this.name,
    required this.type,
    required this.age,
    required this.breed,
    required this.photoPath,
    required this.weight,
    required this.healthStatus,
    required this.status,
  });
}
